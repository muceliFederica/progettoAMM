<h2>Chi siamo</h2>
<p>
La famiglia Orru' dal 19.. si e' completamente dedicata ai sapori della Sardegna. Facendo, dei piatti dei giorni di festa nella nostra isola, un lavoro.
Culurgionis,coccoi de corcoriga , coccoi de patata , sebadas e tanti altri prodotti. 
Il pastificio Orru' e' diverso da tutti gli altri perche preserva i sapori tradizionalie garantisce prodotti di alta qualita.
